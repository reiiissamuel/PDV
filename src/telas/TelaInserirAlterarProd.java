    public TelaInserirAlterarProd(java.awt.Frame parent, OpAuxiliar opAuxiliar, Produto produto) {
         super(parent, true);
        confirmado = false;
        this.opAuxiliar = opAuxiliar;
        this.produto = produto;
        initComponents();
        controleProduto = new ControleProduto(new ArrayList<>());
        
        if (opAuxiliar == opAuxiliar.ocAlterar
                || opAuxiliar == OpAuxiliar.ocConsultar) {
            jLabelTitulo.setText(null);
            jLabelTitulo.setText("Produto");
            jLabelCod.setText(Integer.toString(produto.getCodigo()));
            txtNome.setText(produto.getNome());
            txtPreco.setText(produto.getPreco().toPlainString());
            txtEstoque.setText(Float.toString(produto.getQtdEstoque()));
            txtQtdAbnt.setText(Float.toString(produto.getQtdUnidadeAbt()));
            if(produto.isVendaFraconada() == true){
                jComboBoxVF.removeAllItems();
                jComboBoxVF.insertItemAt("sim", 0);
                jComboBoxVF.insertItemAt("não", 1);
            }else{
                jComboBoxVF.removeAllItems();
                jComboBoxVF.insertItemAt("não", 0);
                jComboBoxVF.insertItemAt("sim", 1);
            }
            
            
        }else{
            jCod.setVisible(false);
            jLabelCod.setVisible(false);
        }
    }
    
    public boolean opConfirmada(){
        return confirmado;
    }
    
