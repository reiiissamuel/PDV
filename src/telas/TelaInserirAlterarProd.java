    private void jButtonConcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConcluirActionPerformed
        if (opAuxiliar == OpAuxiliar.ocCadastrar || opAuxiliar == OpAuxiliar.ocAlterar) {

            if ("".equals(txtNome.getText()) || "".equals(txtPreco.getText())
                    || "".equals(txtEstoque.getText()) || "".equals(txtQtdAbnt.getText())) {

                JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos.");

            } else {

                produto.setNome(txtNome.getText());
                produto.setQtdUnidadeAbt(Float.parseFloat(txtQtdAbnt.getText()));
                BigDecimal b = new BigDecimal(txtPreco.getText());
                produto.setPreco(b);
                produto.setUnidadeAbnt(jComboBoxUnd.getSelectedItem().toString());
                produto.setQtdEstoque(Float.parseFloat(txtEstoque.getText()));

                try {
                    if (jComboBoxVF.getSelectedItem().toString() == "sim") {
                        produto.setVendaFraconada(true);
                    } else if (jComboBoxVF.getSelectedItem().toString() == "não") {
                        produto.setVendaFraconada(false);
                    }
                    if (opAuxiliar == OpAuxiliar.ocAlterar) {
                        JOptionPane.showMessageDialog(null, "Alteração feita com sucesso!");
                    } else {
                        JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");

                    }
                    confirmado = true;
                    dispose();
                } catch (NullPointerException e) {
                    JOptionPane.showMessageDialog(null, "Habilite ou desabile a venda fracionada!");
                }

            }
        }

        
    }//GEN-LAST:event_jButtonConcluirActionPerformed

